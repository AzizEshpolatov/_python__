import math

# bolish usullari
print(10 / 4)  # 2.5
print(10 // 4)  # 2

# Sonning pastki butun qismini qaytaradi.
print(math.floor(3.7))  # 3
# Sonning yuqori butun qismini qaytaradi.
print(math.ceil(3.2))  # 4
# Absolute Value (abs)
res = abs(-10)  # Output: 10 (absolute value of -10)

print(round(3.5))  # 4 (3.5 yuqoriga qarab yaxlitlanadi)
print(round(3.4))  # 3 (3.4 pastga qarab yaxlitlanadi)

